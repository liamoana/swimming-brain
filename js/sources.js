(function loadDefaultSources() {
  const KEY = "sb-sources";
  const existing = JSON.parse(localStorage.getItem(KEY) || "[]");
  if (existing.length > 0) return; // Already has sources

  const sources = DEFAULT_SOURCES.map(src => {
    const id = crypto.randomUUID().slice(0, 12);
    const chunks = Brain.processNote ? null : null; // Will use Brain's chunking

    // Chunk the text manually (matching Brain's logic)
    const CHUNK_SIZE = 1500;
    const CHUNK_OVERLAP = 150;
    const text = (src.title + "\n\n" + src.content).trim();
    const chunked = [];

    if (text.length <= CHUNK_SIZE) {
      chunked.push(text);
    } else {
      let start = 0;
      while (start < text.length) {
        let end = start + CHUNK_SIZE;
        if (end >= text.length) {
          chunked.push(text.slice(start).trim());
          break;
        }
        const slice = text.slice(start, end);
        const paraBreak = slice.lastIndexOf("\n\n");
        if (paraBreak > CHUNK_SIZE * 0.3) {
          end = start + paraBreak + 2;
        } else {
          for (const sep of [". ", "! ", "? ", ".\n", "\n"]) {
            const sentBreak = slice.lastIndexOf(sep);
            if (sentBreak > CHUNK_SIZE * 0.3) {
              end = start + sentBreak + sep.length;
              break;
            }
          }
        }
        const chunk = text.slice(start, end).trim();
        if (chunk) chunked.push(chunk);
        start = end - CHUNK_OVERLAP;
        if (start <= end - CHUNK_SIZE) start = end;
      }
    }

    return {
      id,
      title: src.title,
      type: src.type,
      chunks: chunked,
      chunkCount: chunked.length,
      uploadedAt: Date.now() - Math.random() * 10000
    };
  });

  localStorage.setItem(KEY, JSON.stringify(sources));
})();
